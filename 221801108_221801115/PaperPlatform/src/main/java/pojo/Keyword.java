package pojo;

public class Keyword {
    private String name;
    private int frequency;

    public String getName() {
        return name;
    }

    public int getFrequency() {
        return frequency;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setFrequency(int frequency) {
        this.frequency = frequency;
    }
}
