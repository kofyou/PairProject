package pojo;

public class HotWord {
    private String word;
    private int num;
    private String year;
    private String type;

    public void setWord(String s) {
        this.word = s;
    }

    public void setNum(int n) {
        this.num = n;
    }

    public void setYear(String y) {
        this.year = y;
    }

    public void setType(String t) {
        this.type = t;
    }

    public String getWord() {
        return word;
    }

    public int getNum() {
        return num;
    }

    public String getYear() {
        return year;
    }

    public String getType() {
        return type;
    }
}
