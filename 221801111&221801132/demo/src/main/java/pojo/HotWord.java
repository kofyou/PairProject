package pojo;

public class HotWord {
    private String word;
    private int num;

    public void setWord(String s) {
        this.word = s;
    }

    public void setNum(int n) {
        this.num = n;
    }

    public String getWord() {
        return word;
    }

    public int getNum() {
        return num;
    }
}
