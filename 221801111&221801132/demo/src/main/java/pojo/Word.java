package pojo;

public class Word {
    private String keywords;
    private String year;

    public void setKeywords(String s) {
        this.keywords = s;
    }

    public void setYear(String s) {
        this.year = s;
    }

    public String getKeywords() {
        return keywords;
    }

    public String getYear() {
        return year;
    }
}
