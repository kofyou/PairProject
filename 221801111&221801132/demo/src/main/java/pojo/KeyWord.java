package pojo;

public class KeyWord {
    private String word;
    private String year;

    public void setWord(String s) {
        this.word = s;
    }

    public void setYear(String s) {
        this.year = s;
    }

    public String getWord() {
        return word;
    }

    public String getYear() {
        return year;
    }
}
